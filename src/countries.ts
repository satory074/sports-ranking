const byEnName: Map<string, string> = new Map();
for (const [code, info] of Object.entries(COUNTRIES)) {
  const key = info.en.toLowerCase();
  if (!byEnName.has(key)) byEnName.set(key, code);
}

/** 英語国名 → コード。解決できない場合は undefined */
export function codeByEnName(name: string): string | undefined {
  const key = name.trim().toLowerCase();
  return byEnName.get(key) ?? NAME_ALIASES[key];
}

export function countryJa(code: string): string | undefined {
  return COUNTRIES[code]?.ja;
}

export function countryFlag(code: string): string {
  return COUNTRIES[code]?.flag ?? '';
}
